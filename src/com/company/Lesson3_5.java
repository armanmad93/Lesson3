package com.company;

public class Lesson3_5 {

    public static void main(String[] args) {

        //Write a program to find the sum of the first 100 natural numbers.

        int sum = 0;

        for (int i = 1; i <= 100; i++) {
            sum += i;
        }
        System.out.println("sum of the first 100 natural numbers is: " + sum);

    }
}
